function sorting_fun(){

    var sorting_value=document.querySelector("#for_sorting").value
    if(sorting_value=="price_high-low"){
        womensproductdata.sort(function(a,b){
            return b.price-a.price
        })
        displaywomens(womensproductdata)
    }
    if(sorting_value=="price_low-high"){
        womensproductdata.sort(function(a,b){
            return a.price-b.price
        })
        displaywomens(womensproductdata)
    }
}
