        //    Womens Product Data Display Function

function displaywomens(data){

    document.querySelector("#products_container").innerHTML=null
    data.forEach(function(el){

        var product_box = document.createElement("div")

        var image = document.createElement("img")
        image.setAttribute("src",el.imageurl)
        var name = document.createElement("div")
        name.classList.add("product_name")
        name.innerText = el.name
        

        var cartimg = document.createElement("img")
        cartimg.setAttribute("src","https://img.shop.com/Image/resources/images/onecart-icon.svg")
        var seller = document.createElement("p")
        seller.innerText = el.seller

        var offprice_price = document.createElement("p")
        var offprice = document.createElement("span")
        offprice.innerText = el.offprice
        var dollarsign = document.createElement("span")
        dollarsign.innerText = "$"
        dollarsign.style.fontWeight="bold"
        dollarsign.style.fontSize="15px"
        var price = document.createElement("span")
        price.classList.add("price")
        price.innerText = el.price
        offprice_price.append(offprice,dollarsign,price)

        var cashback = document.createElement("p")
        cashback.classList.add("cashback_text")
        cashback.innerText = el.cashback
        var cashbacksign = document.createElement("img")
        cashbacksign.setAttribute("src","https://img.shop.com/Image/resources/images/cashback-icon.svg")
        var cashbacktext = document.createElement("span")
        cashbacktext.innerText = "Cashback"

        var buttonbox = document.createElement("div")
        buttonbox.classList.add("products_button")
        var button1 = document.createElement("div")
        button1.innerText = "Add To Cart"
        button1.addEventListener("click",function(){
            addtocart_fun(el)
        })
        var button2 = document.createElement("div")
        button2.id= "buying_button"
        button2.innerText = "Buying Options"
        button2.addEventListener("click",function(){
            buying_options_fun()
        })
        buttonbox.append(button1,button2)

        product_box.append(image,name,cartimg,seller,offprice_price,cashback,cashbacksign,cashbacktext,buttonbox)
        document.querySelector("#products_container").append(product_box)            
    })
}
